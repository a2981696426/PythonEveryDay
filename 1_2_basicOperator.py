# + - * / ** % //
# 加、减、乘、除、指数、取余、地板除
a = 6
b = 4
print("---------------算术-------------------")
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a ** b)
print(a % b)
print(a // b)
print("---------------比较-------------------")

# == != > < >= <=
# 等于、不等、大于、小于、大于等于、小于等于
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print("---------------逻辑-------------------")


# and or not
# 并集、交集、取反
x = a == b
y = a != b

print(x and y)
print(x or y)
print(not x)
